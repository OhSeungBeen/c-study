// 비트 연산자
#include<stdio.h>

int main() {

	int a = 10;
	int b = 12;
	// a	   10	0000 0000 0000 0000 0000 1010
	// b	   12	0000 0000 0000 0000 0000 1100
	// a & b   8	0000 0000 0000 0000 0000 1000
	// a ^ b   6    0000 0000 0000 0000 0000 0110
	// a | b   14   0000 0000 0000 0000 0000 1110
	// ~a	  -11	1111 1111 1111 1111 1111 0001
	// a << 1  20	0000 0000 0000 0000 0001 0100
	// a >> 2  2	0000 0000 0000 0000 0000 0010
	printf("a & b : %d\n", a & b);
	printf("a ^ b : %d\n", a ^ b);
	printf("a | b : %d\n", a | b);
	printf("~a : %d\n", ~a);
	printf("a << 1 : %d\n", a << 1);
	printf("a >> 2 : %d\n", a >> 2);

	return 0;
}